class Pessoa {
  String? name;
  double? peso;
  double? altura;

  void setName(String name) {
    name = name;
  }

  getName(String name) {
    return name;
  }

  void setPeso(double peso) {
    peso = peso;
  }

  getPeso(double peso) {
    return peso;
  }

  void setAltura(double altura) {
    altura = altura;
  }

  getAltura(double altura) {
    return altura;
  }
}
